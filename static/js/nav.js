$.extend(Nav.prototype,{
	createDom:function(){
		var navDom = document.createElement("div");
		navDom.innerHTML=shtml;
		$(this.container).html(navDom);
		var text = 'area_list';
		if( window.location.search ){
			text = window.location.search.split("=")[1];
			if(text.indexOf('&')!=-1){
				text = text.replace(/&id=*/,'');
			}
		}

		$.each($(".JS-nav a"),function(index,item ){
			var a_text = $(item).attr("href");
			var active = '';
			if(a_text && a_text != '#' ){
				active = a_text.split("=")[1];
			}

			if( active == text ){
				$(item).closest("li").parents("li").addClass("open active");
			}
		})
	}
})